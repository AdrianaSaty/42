void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int c;
	int	temp;

	i = 0;
	c = size - 1;
	while (i < c)
	{
		temp = tab[i];
		tab[i] = tab[c];
		tab[c] = temp;
		i++;
		c--;
	}
}
